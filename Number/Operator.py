
# 오칙연산
a1 = int(input())               # 7
a2 = int(input())               # 3

print(a1, "+", a2, "=", a1+a2)   # 10
print(a1, "-", a2, "=", a1-a2)   # 4
print(a1, "*", a2, "=", a1*a2)   # 21
print(a1, "/", a2, "=", a1/a2)   # 2.3333..  (java는 몫연산)
print(a1, "//", a2, "=", a1//a2) # 2    (이게 몫!!)
print(a1, "%", a2, "=", a1%a2)   # 1